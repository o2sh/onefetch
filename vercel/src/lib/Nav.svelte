  nav {
    display: flex;
    flex-direction: row;
    justify-content: space-around;
    align-items: center;
    padding: 0.5rem;
    border-bottom: 1px solid #ccc;
  }

  .active {
    color: #006994;
    border-bottom: 2px solid #4a4a4a;
  }
</style>
